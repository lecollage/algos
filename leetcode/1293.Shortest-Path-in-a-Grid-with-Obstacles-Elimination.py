from typing import List
from queue import PriorityQueue, Queue

class Solution:
    def shortestPath(self, grid: List[List[int]], max_obstacles: int) -> int:
        n = len(grid)
        m = len(grid[0])
        directions = [
            [-1,0],
            [0,-1],
            [1,0],
            [0,1],
        ]

        def isValid(i: int, j: int) -> bool:
            if i < 0 or i >= len(grid):
                return False
            
            if j < 0 or j >= len(grid[i]):
                return False
            
            return True
        
        queue = Queue()
        visited = [[max_obstacles for _ in range(m)] for _ in range(n)]

        print(visited)

        queue.put((0, 0, 0))

        if grid[0][0] == 1:
            visited[0][0] -= 1

        while not queue.empty():
            i, j, distance = queue.get()

            print('step1: ', i, j, distance, visited)

            if i == n-1 and j == m-1:
                return distance
            
            if visited[i][j] < 0: 
                continue

            for dirI, dirJ in directions:
                nextI = i+dirI
                nextJ = j+dirJ

                if isValid(nextI, nextJ) and visited[i][j]-grid[nextI][nextJ] >= 0:
                    queue.put((nextI, nextJ, distance+1))
                    visited[nextI][nextJ] = visited[i][j]-grid[nextI][nextJ]

        return -1

