from typing import List
from queue import PriorityQueue, Queue

class Solution:
    def shortestPath(self, grid: List[List[int]], max_obstacles: int) -> int:
        n = len(grid)
        m = len(grid[0])
        directions = [
            [-1,0],
            [0,-1],
            [1,0],
            [0,1],
        ]

        def isValid(i: int, j: int) -> bool:
            return i in range(n) and j in range(m)
        
        queue = Queue()
        visited = [[max_obstacles+1 for _ in range(m)] for _ in range(n)]

        print(visited)

        queue.put((0, 0, 0, 0)) # i, j, distance, weights_sum

        while not queue.empty():
            i, j, distance, weight = queue.get()
           
            if i == n-1 and j == m-1:
                return distance
            
            for di, dj in directions:
                nextI = i+di
                nextJ = j+dj

                if not isValid(nextI, nextJ):
                    continue

                nextWeight = weight + grid[nextI][nextJ]

                if nextWeight < visited[nextI][nextJ]:
                    queue.put((nextI, nextJ, distance + 1, nextWeight))
                    visited[nextI][nextJ] = nextWeight
                
        return -1
        


