from typing import List

class Solution:
    def largestRectangleArea(self, nums: List[int]) -> int:
        if len(nums) == 0:
            return 0


        lefts = [-1] * len(nums)
        rights = [-1] * len(nums)

        stack = [-1]

        for i in range(len(nums)):
            while len(stack) > 1 and nums[stack[-1]] >= nums[i]:
                stack.pop()
        
            lefts[i] = stack[-1]
            stack.append(i)

        stack = [len(nums)]

        for i in range(len(nums)-1, -1, -1):
            while len(stack) > 1 and nums[stack[-1]] >= nums[i]: # nums[2]
                stack.pop()

            rights[i] = stack[-1]
            stack.append(i)

        print(lefts)
        print(rights)

        maxSquare = 0

        for i in range(len(nums)):
            square = nums[i] * (rights[i] - lefts[i] - 1)
            maxSquare = max(square, maxSquare)

        return maxSquare

